//////////////////////// define widgets & map message ////////////////////////
enum WND_ID
{
    ID_ROOT = 1,
    ID_WAVE1,
    ID_WAVE2,
    ID_WAVE3,
    ID_BUTTON
};

typedef struct c_my_ui
{
    c_wnd base;

    c_wave_buffer m_wave_buffer1;
    c_wave_buffer m_wave_buffer2;
    c_wave_buffer m_wave_buffer3;
} c_my_ui;

void c_my_ui_on_clicked(c_wnd *self, int ctrl_id, int param);

void c_my_ui_vir_on_init_children(c_wnd *self)
{
    c_my_ui *my_ui = (c_my_ui *)self;
    c_button_set_on_click(((c_button *)c_wnd_get_wnd_ptr(self, ID_BUTTON)), c_my_ui_on_clicked);

    c_wave_ctrl *p_wave = (c_wave_ctrl *)c_wnd_get_wnd_ptr(self, ID_WAVE1);
    c_wave_ctrl_set_wave_speed(p_wave, 1);
    c_wave_ctrl_set_wave_color(p_wave, GL_RGB(0, 255, 0));
    c_wave_ctrl_set_wave_in_out_rate(p_wave, 256, 17);
    c_wave_ctrl_set_wave(p_wave, &my_ui->m_wave_buffer1);
    c_wave_ctrl_set_max_min(p_wave, 170, 110);

    p_wave = (c_wave_ctrl *)c_wnd_get_wnd_ptr(self, ID_WAVE2);
    c_wave_ctrl_set_wave_speed(p_wave, 1);
    c_wave_ctrl_set_wave_color(p_wave, GL_RGB(41, 189, 189));
    c_wave_ctrl_set_wave_in_out_rate(p_wave, 60, 17);
    c_wave_ctrl_set_wave(p_wave, &my_ui->m_wave_buffer2);
    c_wave_ctrl_set_max_min(p_wave, 100, 0);

    p_wave = (c_wave_ctrl *)c_wnd_get_wnd_ptr(self, ID_WAVE3);
    c_wave_ctrl_set_wave_speed(p_wave, 1);
    c_wave_ctrl_set_wave_color(p_wave, GL_RGB(255, 255, 0));
    c_wave_ctrl_set_wave_in_out_rate(p_wave, 128, 17);
    c_wave_ctrl_set_wave(p_wave, &my_ui->m_wave_buffer3);
    c_wave_ctrl_set_max_min(p_wave, 180, 80);
}

void c_my_ui_vir_on_paint(c_wnd *self)
{
    c_rect rect;
    c_wnd_get_screen_rect(self, &rect);
    c_surface_fill_rect_ex(self->m_surface, rect, GL_RGB(33, 33, 33), self->m_z_order);
}

void c_my_ui_on_clicked(c_wnd *self, int ctrl_id, int param)
{
    static int index;
    c_rect rect;
    c_wnd_get_screen_rect(self, &rect);
    c_button *button = (c_button *)c_wnd_get_wnd_ptr(self, ID_BUTTON);
    switch (++index % 3)
    {
    case 0:
        c_wnd_set_str(&button->base, "GuiLite");
        break;
    case 1:
        c_wnd_set_str(&button->base, "\xe2\x9d\xa4");
        break;
    default:
        c_wnd_set_str(&button->base, "\xe5\x8d\x95\xe7\x89\x87\xe6\x9c\xba");
        break;
    }
    button->base.m_api->show_window(&button->base);
}

static const c_wnd_vir_api c_my_ui_vir_api_table = {
        c_wnd_vir_connect,     c_my_ui_vir_on_init_children, c_my_ui_vir_on_paint, c_wnd_vir_show_window,   c_wnd_vir_on_touch,
        c_wnd_vir_on_navigate, c_wnd_vir_pre_create_wnd,     c_wnd_vir_on_focus,   c_wnd_vir_on_kill_focus,
};

void c_my_ui_init(c_my_ui *self)
{
    c_wnd_init(&self->base);
    self->base.m_api = &c_my_ui_vir_api_table;

    c_wave_buffer_init(&self->m_wave_buffer1);
    c_wave_buffer_init(&self->m_wave_buffer2);
    c_wave_buffer_init(&self->m_wave_buffer3);
}

